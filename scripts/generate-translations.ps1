<?xml version="1.0" encoding="utf-8"?>
<root>
  <xsd:schema id="root" xmlns="" xmlns:xsd="http://www.w3.org/2001/XMLSchema" xmlns:msdata="urn:schemas-microsoft-com:xml-msdata">
    <xsd:element name="root" msdata:IsDataSet="true">
      <xsd:complexType>
        <xsd:choice maxOccurs="unbounded">
          <xsd:element name="data">
            <xsd:complexType>
              <xsd:sequence>
                <xsd:element name="value" type="xsd:string" minOccurs="0" msdata:Ordinal="1" />
              </xsd:sequence>
              <xsd:attribute name="name" type="xsd:string" use="required" />
              <xsd:attribute name="type" type="xsd:string" />
              <xsd:attribute name="xml:space" type="xsd:string" />
            </xsd:complexType>
          </xsd:element>
        </xsd:choice>
      </xsd:complexType>
    </xsd:element>
  </xsd:schema>
  <resheader name="resmimetype"><value>text/microsoft-resx</value></resheader>
  <resheader name="version"><value>2.0</value></resheader>
  <resheader name="reader"><value>System.Resources.ResXResourceReader</value></resheader>
  <resheader name="writer"><value>System.Resources.ResXResourceWriter</value></resheader>
'@

function Write-ResxFile($lang, $translations) {
    $path = Join-Path $resDir "SharedResource.$lang.resx"
    $sb = New-Object System.Text.StringBuilder
    [void]$sb.AppendLine($header)
    foreach ($key in ($translations.Keys | Sort-Object)) {
        $val = $translations[$key]
        # XML-escape the value
        $val = $val -replace '&', '&amp;'
        $val = $val -replace '<', '&lt;'
        $val = $val -replace '>', '&gt;'
        $val = $val -replace '"', '&quot;'
        # Don't double-escape if already escaped
        [void]$sb.AppendLine("  <data name=`"$key`" xml:space=`"preserve`"><value>$val</value></data>")
    }
    [void]$sb.AppendLine("</root>")

    $utf8NoBom = New-Object System.Text.UTF8Encoding $false
    [System.IO.File]::WriteAllText($path, $sb.ToString(), $utf8NoBom)
    Write-Host "Written $path with $($translations.Count) keys"
}

function Read-ExistingResx($lang) {
    $path = Join-Path $resDir "SharedResource.$lang.resx"
    $result = @{}
    if (Test-Path $path) {
        [xml]$xml = Get-Content $path -Encoding UTF8
        foreach ($d in $xml.root.data) {
            $result[$d.name] = $d.value
        }
    }
    return $result
}

