foreach ($lang in @('zh','de','es','fr','hi','it','ja','ko','pt','ru','id','nl','tr','ar')) {
    Write-Host "Processing $lang..."

    # Read existing translations
    $existing = Read-ExistingResx $lang

    # Start with existing translations
    $allTranslations = @{}
    foreach ($k in $existing.Keys) {
        $allTranslations[$k] = $existing[$k]
    }

    # Add new translations from our definitions
    if ($newTranslations.ContainsKey($lang)) {
        foreach ($k in $newTranslations[$lang].Keys) {
            if (-not $allTranslations.ContainsKey($k)) {
                $allTranslations[$k] = $newTranslations[$lang][$k]
            }
        }
    }

    # Verify we have all English keys
    $missing = @()
    foreach ($k in $enKeys.Keys) {
        if (-not $allTranslations.ContainsKey($k)) {
            $missing += $k
        }
    }

    if ($missing.Count -gt 0) {
        Write-Host "  WARNING: $lang still missing $($missing.Count) keys!"
        foreach ($k in $missing) {
            Write-Host "    Missing: $k"
        }
    }

    Write-Host "  Total keys: $($allTranslations.Count)"
    Write-ResxFile $lang $allTranslations
}

