        Sb.AppendLine("        }");
        Sb.AppendLine("        return Bytes;");
        Sb.AppendLine("    }");
        Sb.AppendLine();
        Sb.AppendLine("    private static string IdentifyNetwork(byte[] Aid)");
        Sb.AppendLine("    {");
        Sb.AppendLine("        if (Aid.Length >= 5 && Aid[0] == 0xA0 && Aid[1] == 0x00 && Aid[2] == 0x00 && Aid[3] == 0x00)");
