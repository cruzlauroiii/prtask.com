        Sb.AppendLine("                0x65 => \"JCB\",");
        Sb.AppendLine("                _ => \"Unknown\",");
        Sb.AppendLine("            };");
        Sb.AppendLine("        }");
        Sb.AppendLine("        return \"Unknown\";");
        Sb.AppendLine("    }");

        Sb.AppendLine();
        Sb.AppendLine("    private static bool IsSuccess(byte[]? Response) => Response is { Length: >= 2 } && Response[^2] == 0x90 && Response[^1] == 0x00;");
        Sb.AppendLine("    private static string MaskPan(string Pan) => Pan.Length > 8 ? string.Concat(Pan.AsSpan(0, 4), new string('*', Pan.Length - 8), Pan.AsSpan(Pan.Length - 4)) : Pan;");
        Sb.AppendLine("    private static string ToHex(byte[] Bytes) => System.Convert.ToHexString(Bytes);");
