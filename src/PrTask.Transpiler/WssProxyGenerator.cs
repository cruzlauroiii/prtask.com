        Sb.AppendLine("            if (Val.Length != Len) Val = new byte[Len];");
        Sb.AppendLine("            DataMs.Write(Val, 0, Val.Length);");
        Sb.AppendLine("        }");
        Sb.AppendLine("        var PdolData = DataMs.ToArray();");
        Sb.AppendLine("        var Cmd = new byte[7 + PdolData.Length];");
        Sb.AppendLine("        Cmd[0] = 0x80;");
        Sb.AppendLine("        Cmd[1] = 0xA8;");
        Sb.AppendLine("        Cmd[2] = 0x00;");
        Sb.AppendLine("        Cmd[3] = 0x00;");
