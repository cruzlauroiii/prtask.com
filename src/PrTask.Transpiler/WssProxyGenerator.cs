        Sb.AppendLine("        Ms.Write(UnpredNum, 0, 4);");
        Sb.AppendLine("        return Ms.ToArray();");
        Sb.AppendLine("    }");
        Sb.AppendLine();
        Sb.AppendLine("    private static byte[] HexToBytes(string Hex)");
        Sb.AppendLine("    {");
