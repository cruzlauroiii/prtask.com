        Sb.AppendLine("    private static byte[]? FindTag(byte[] Data, byte Tag1, byte Tag2 = 0)");
        Sb.AppendLine("    {");
        Sb.AppendLine("        for (var I = 0; I < Data.Length - 2;)");
        Sb.AppendLine("        {");
        Sb.AppendLine("            var IsMultiByte = (Data[I] & 0x1F) == 0x1F;");
        Sb.AppendLine("            var T1 = Data[I];");
        Sb.AppendLine("            byte T2 = 0;");
        Sb.AppendLine("            I++;");
        Sb.AppendLine("            if (IsMultiByte && I < Data.Length)");
        Sb.AppendLine("            {");
        Sb.AppendLine("                T2 = Data[I];");
        Sb.AppendLine("                I++;");
        Sb.AppendLine("            }");
        Sb.AppendLine("            if (I >= Data.Length)");
        Sb.AppendLine("            {");
        Sb.AppendLine("                break;");
        Sb.AppendLine("            }");
        Sb.AppendLine("            var Len = Data[I++];");
        Sb.AppendLine("            if (Len == 0x81 && I < Data.Length)");
        Sb.AppendLine("            {");
        Sb.AppendLine("                Len = Data[I++];");
        Sb.AppendLine("            }");
        Sb.AppendLine("            else if (Len == 0x82 && I + 1 < Data.Length)");
        Sb.AppendLine("            {");
        Sb.AppendLine("                Len = (byte)((Data[I] << 8) | Data[I + 1]);");
        Sb.AppendLine("                I += 2;");
        Sb.AppendLine("            }");
        Sb.AppendLine("            if (I + Len > Data.Length)");
        Sb.AppendLine("            {");
        Sb.AppendLine("                break;");
        Sb.AppendLine("            }");
        Sb.AppendLine("            if ((Tag2 == 0 && T1 == Tag1 && !IsMultiByte) || (Tag2 != 0 && T1 == Tag1 && T2 == Tag2))");
        Sb.AppendLine("            {");
        Sb.AppendLine("                var Val = new byte[Len];");
        Sb.AppendLine("                System.Array.Copy(Data, I, Val, 0, Len);");
        Sb.AppendLine("                return Val;");
        Sb.AppendLine("            }");
        Sb.AppendLine("            if (T1 is 0x70 or 0x77 or 0x61 or 0x6F or 0xA5)");
        Sb.AppendLine("            {");
        Sb.AppendLine("                var Nested = new byte[Len];");
        Sb.AppendLine("                System.Array.Copy(Data, I, Nested, 0, Len);");
        Sb.AppendLine("                var Found = FindTag(Nested, Tag1, Tag2);");
        Sb.AppendLine("                if (Found != null)");
        Sb.AppendLine("                {");
        Sb.AppendLine("                    return Found;");
        Sb.AppendLine("                }");
        Sb.AppendLine("            }");
        Sb.AppendLine("            I += Len;");
        Sb.AppendLine("        }");
        Sb.AppendLine("        return null;");
        Sb.AppendLine("    }");
        Sb.AppendLine();
        Sb.AppendLine("    private static byte[] BuildCdol1(long AmountCents, int CurrencyCode)");
        Sb.AppendLine("    {");
        Sb.AppendLine("        var Now = System.DateTime.UtcNow;");
        Sb.AppendLine("        var Rng = new System.Random();");
